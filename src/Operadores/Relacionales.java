package Operadores;

public class Relacionales {
	// Estas son las operaciones relacionales
	public static void main(String[] args) {
		int m = 2, n = 5;
		boolean res;
		res = m > n;  //M es mayor que n?
		res = m < n; // m es menor que n?
		res = m >= n; // m es mayor igual que n?
		res = m <= n; // m es menor igual que n?
		res = m == n; // m es igual que n?
		res = m != n; // m diferente que n?
	}
}

	
